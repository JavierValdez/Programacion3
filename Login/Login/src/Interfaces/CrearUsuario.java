    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        usuarioNuevo.setUser(NuevoUser1.getText());
        usuarioNuevo.setPassword(NuevaContraseña.getText());
        usuarioNuevo.setTipoUsuario(ComboBoxTipoUsuario.getSelectedItem().toString());//la opcion seleccionada da un objeto, el cual se convierte en string que se asigna a una varible
        boolean alfaUser = Pattern.matches("^[a-zA-Z0-9]*$", usuarioNuevo.getUser());//true si todo es alfanumerico
        boolean alfaPassword=Pattern.matches("^[a-zA-Z0-9]*$", usuarioNuevo.getPassword());//true si todo es alfanumerico

        if(alfaUser==true && alfaPassword==true && !usuarioNuevo.getUser().equals("") && !usuarioNuevo.getPassword().equals("")){
        try {
            guardar.RecibirUsuario(usuarioNuevo);
            JOptionPane.showMessageDialog(null,"Se ha guardado con exito el usuario");
            this.setVisible(false);
            inicio.setVisible(true);
            
        } catch (IOException ex) {
            System.out.println("Error al guardar");
        }
        }else{
            JOptionPane.showMessageDialog(null,"Datos ingresados no validos");
            NuevoUser1.setText(null);
            NuevaContraseña.setText(null);
        }
        
    }//GEN-LAST:event_BtnGuardarActionPerformed
        
       
    
