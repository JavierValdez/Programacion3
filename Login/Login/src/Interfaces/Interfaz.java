    @SuppressWarnings("StringEquality")
    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed
        nuevoUsuario.setUser(UsuarioTxt.getText());//usuario es el boton en el formulario, y el metodo getText almacena el valor en la variable usuario 
        nuevoUsuario.setPassword(ContraseñaTxt.getText());
        try {
            nuevoUsuario = buscar.BuscarUser(nuevoUsuario);
        } catch (IOException | ClassNotFoundException ex) {
            System.out.println("Error");
        }
        
        switch(nuevoUsuario.getTipoUsuario()){
            case "ADMINISTRADOR":
                AccesoAdministrador accesoCorrectoAdministrador = new AccesoAdministrador(nuevoUsuario.getUser()); //creando un objeto accesoCorrecto para poder acceder al formulario
                accesoCorrectoAdministrador.setVisible(true);//muestra el siguiente formulario, el que se instanceo en el objto accesoCorrecto
                dispose();
                break;
            case "ESCRITORIO":
                AccesoEscritorio acceso = new AccesoEscritorio(nuevoUsuario.getUser());
                acceso.setVisible(true);
                dispose();
        }
    }//GEN-LAST:event_BtnIngresarActionPerformed
        
